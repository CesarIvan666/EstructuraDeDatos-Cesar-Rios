package utils;
/**
 * Enumeración que representa diferentes métodos de ordenación.
 */
public enum SortingMethods {
    /**
     * Algoritmo de ordenación de burbuja.
     */
    BUBBLE,

    /**
     * Algoritmo de ordenación por selección.
     */
    SELECTION,

    /**
     * Algoritmo de ordenación por inserción.
     */
    INSERTION,

    /**
     * Algoritmo de ordenación Shell Sort.
     */
    SHELL,

    /**
     * Algoritmo de ordenación Merge Sort.
     */
    MERGE,

    /**
     * Algoritmo de ordenación Quick Sort.
     */
    QUICK,

    /**
     * Algoritmo de ordenación Heap Sort.
     */
    HEAP
}

